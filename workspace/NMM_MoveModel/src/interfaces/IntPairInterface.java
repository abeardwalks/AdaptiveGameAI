package interfaces;

/**
 * Interface for an integer pair, this is used for movements, that store a 
 * from & to index. 
 * 
 * @author Andrew White - BSc Software Engineering, 200939787
 *
 */
public interface IntPairInterface {
	
	/**
	 * @return - The first integer in the pair.
	 */
	int getFirstInt();
	
	/**
	 * @return - The second integer in the pair. 
	 */
	int getSecondInt();
}
